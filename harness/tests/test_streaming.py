# Subscription mechanism — bypass HTTP, exercise the Session class directly
# ---------------------------------------------------------------------------

def _run_async(coro):
    """Run an async coroutine to completion in a fresh loop."""
    return asyncio.run(coro)


def test_subscribe_delivers_press_events(tmp_path):
    """The subscriber queue should receive events generated by press_button.

    press_button is called synchronously from inside the test; the queue
    is asyncio.Queue but the publisher uses call_soon_threadsafe — so we
    run the whole test in an async loop with a separate thread for the press.
    """
    base_dir = tmp_path / "sessions"
    session = Session.create(base_dir, ROM_PATH, snapshot_interval_frames=0)
    try:
        async def driver():
            queue = session.subscribe_events()
            try:
                # Run the press in a thread so the loop stays alive to receive
                # publish notifications via call_soon_threadsafe.
                def press_work():
                    # Boot to a state where text_display fires
                    session.wait(900)
                    return session.press_button("start", hold_frames=10, release_frames=80)

                events = await asyncio.to_thread(press_work)
                # The publisher used call_soon_threadsafe; pump the loop briefly
                # so scheduled put_nowait() calls actually land in the queue.
                await asyncio.sleep(0.1)
                # Drain the queue
                drained: list = []
                while not queue.empty():
                    drained.append(await queue.get())
                return events, drained
            finally:
                session.unsubscribe_events(queue)

        events, drained = _run_async(driver())
        # The subscriber received events from BOTH wait(900) and the press;
        # `events` only contains the press's return value. Both should be
        # non-empty.
        assert len(drained) > 0
        # The press's events should be a subset of what the subscriber saw.
        event_ids = {e.id for e in events}
        drained_ids = {e.id for e in drained}
        if event_ids:
            assert event_ids.issubset(drained_ids), \
                f"press events {event_ids} not seen by subscriber {drained_ids}"
    finally:
        session.end()


def test_unsubscribe_stops_delivery(tmp_path):
    """After unsubscribe, no new events should appear in the queue."""
    base_dir = tmp_path / "sessions"
    session = Session.create(base_dir, ROM_PATH, snapshot_interval_frames=0)
    try:
        async def driver():
            queue = session.subscribe_events()
            session.unsubscribe_events(queue)
            # Generate events
            await asyncio.to_thread(session.wait, 900)
            await asyncio.sleep(0.05)
            return queue.empty()

        assert _run_async(driver())
    finally:
        session.end()
