def test_snapshots_poll_get_current(client, session_id):
    r = client.post("/snapshots/poll", headers={SESSION_HEADER: session_id}, json={})
    assert r.status_code == 200
    assert r.json()["interval_frames"] == 0


def test_snapshots_poll_set_persists(client, session_id):
    r = client.post("/snapshots/poll", headers={SESSION_HEADER: session_id},
                    json={"interval_frames": 30})
    assert r.status_code == 200
    assert r.json() == {"interval_frames": 30, "previous": 0}

    # Verify meta.json was updated on disk
    registry = client.app.state.registry
    folder = registry.base_dir / session_id
    meta = json.loads((folder / "meta.json").read_text())
    assert meta["snapshot_interval_frames"] == 30

    # And via the API
    r = client.post("/snapshots/poll", headers={SESSION_HEADER: session_id}, json={})
    assert r.json()["interval_frames"] == 30


def test_snapshots_poll_validates_negative(client, session_id):
    r = client.post("/snapshots/poll", headers={SESSION_HEADER: session_id},
                    json={"interval_frames": -1})
    assert r.status_code == 400


